							<ol>
								<li>Provides <u>fixed monetary awards</u> for employees injured on the job.</li>
									<ul>
										<li><u>Eliminates need for litigation</u>. Creates an easier process for the employee.</li>
										<li><u>Helps control employers' financial risks</u> due to many states limiting how much an employee can recover from an employee.</li>
									</ul>
								<li><u>Many states require some form of it.</u></li>
								<li><b>Not</b> health insurance. For injuries sustained on the job.</li>
								<li>In states that don't require it, still <u>good to have</u> if employees engage in <u>hazardous activities</u>.</li>
							</ol>
